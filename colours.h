#define __SET_GREEN printf("\033[32m");
#define __SET_MAGENTA printf("\033[95m");
#define __SET_YELLOW printf("\033[93m");
#define __SET_BLUE printf("\033[94m");
#define __SET_DEFAULT printf("\033[0m");

#endif /* COLOURS_H */
